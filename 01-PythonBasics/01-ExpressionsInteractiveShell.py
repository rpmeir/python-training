# Math Operators

print('# Math Operators')
# Exponent
print('>>> 2 ** 3')
print(2 ** 3)
# Modulus
print('>>> 22 % 8') 
print(22 % 8)
# Integer Division
print('>>> 22 // 8')
print(22 // 8)
# Division
print('>>> 22 / 8')
print(22 / 8)
# Multiplication
print('>>> 3 * 5')
print(3 * 5)
# Subtraction
print('>>> 5 - 2')
print(5 - 2)
# Addition
print('>>> 2 + 2')
print(2 + 2)

# Strings Concatenation and Replication
print('Strings Concatenation and Replication')
print('>>> \'Alice\' + \'Bob\'')
print('Alice' + 'Bob')
print('>>> \'Alice\' * 5')
print('Alice' * 5)

# Variables and Assignments
print('Assignments:')
spam = 40
print('>>> spam =', spam)
eggs = 15
print('>>> eggs =', eggs)
print('>>> spam + eggs')
print(spam + eggs)
spam = spam + eggs
print('>>> spam = spam + eggs')
print('>>> spam')
print(spam)
spam = 'Hello'
print('>>> spam = \'Hello\'')
print('>>> spam')
print(spam)
